// This file is a simple demonstration that Rust
// allows for the testing of private functions, like
// `internal_adder`, which we can call directly within
// the `tests` mod scope
pub fn add_two(a: i32) -> i32 {
    internal_adder(a, 2)
}

fn internal_adder(a: i32, b: i32) -> i32 {
    a + b
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn internal() {
        assert_eq!(4, internal_adder(2, 2));
    }
}
